// ======================= СИД =======================
async function main() {
  // Города
  for (const c of cities) {
    await prisma.city.upsert({
      where: { slug: c.slug },
      update: c,
      create: c,
    });
  }

  // Услуги и категории
  for (const s of services) {
    await prisma.service.upsert({
      where: { slug: slugify(s.name, { lower: true, strict: true }) },
      update: {},
      create: {
        slug: slugify(s.name, { lower: true, strict: true }),
        name: s.name,
        isCoverser: s.isCoverser,
        categories: {
          connectOrCreate: s.categories.map((cat) => ({
            where: { name: cat },
            create: { name: cat },
          })),
        },
      },
    });
  }

  console.log(`Seeded ${services.length} services with categories.`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
